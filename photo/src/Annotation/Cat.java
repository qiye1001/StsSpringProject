package Annotation;

import java.util.Arrays;

@MyAnnotation(name = "Marry", color = Color.GRREEN, like = { "eat", "eat", "eat" })
public class Cat {
	private String name;
	private int age;
	private Color color;
	private String[] like;

	
	
	public Cat() {
		super();
	}

	public Cat(String name, int age, Color color, String[] like) {
		super();
		this.name = name;
		this.age = age;
		this.color = color;
		this.like = like;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public Color getColor() {
		return color;
	}

	public void setColor(Color color) {
		this.color = color;
	}

	public String[] getLike() {
		return like;
	}

	public void setLike(String[] like) {
		this.like = like;
	}

	@Override
	public String toString() {
		return "Cat [name=" + name + ", age=" + age + ", color=" + color + ", like=" + Arrays.toString(like) + "]";
	}

}
